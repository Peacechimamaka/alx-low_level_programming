#include <stdio.h>

/**
 * main - function that prints the sizes of data types
 *
 * Return: 0
 */
int main(void)
{
	printf("Size of an int is: %ld byte(s)\n", sizeof(int));
	printf("Size of a char: %ld byte(s)\n", sizeof(char));
	printf("Size of long int is: %ld byte(s)\n", sizeof(long int));
	printf("Size of long long int is: %ld byte(s$\n", sizeof(long long int));
	printf("Size of float is: %ld byte(s)\b", sizeof(float));
}
